package cv2;

public class Adresar {
	private Adresar left;
	private Adresar right;
	
	public Adresar getLeft() {
		return left;
	}
	public void setLeft(Adresar left) {
		this.left = left;
	}
	public Adresar getRight() {
		return right;
	}
	public void setRight(Adresar right) {
		this.right = right;
	}
	
	
}
